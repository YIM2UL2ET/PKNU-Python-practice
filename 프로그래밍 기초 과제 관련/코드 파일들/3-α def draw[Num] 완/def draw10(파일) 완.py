import turtle as t

t.speed(0)
t.penup()
t.hideturtle()

def draw10(t):
    t.pensize(4)
    t.penup()
    t.bk(5)
    t.lt(90)
    t.bk(10)
    t.pendown()
    t.fd(20)
    t.rt(30)
    t.bk(5)
    t.rt(60)
    t.penup()
    t.fd(13)
    t.lt(90)
    t.bk(16)
    t.rt(90)
    t.pendown()
    t.circle(6,80)
    t.fd(5)
    t.lt(20)
    t.fd(5)
    t.circle(6,160)
    t.fd(5)
    t.lt(20)
    t.fd(5)
    t.circle(6,80)

draw10(t)
